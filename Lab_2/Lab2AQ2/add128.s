	ENTRY
Reset_Handler
	LDR R0,= VALUE1
	LDR R1,= VALUE2
	
	LDR R2,[R0,#12]
	LDR R3,[R1,#12]
	ADDS R4,R2,R3
	
	LDR R2,[R0,#8]
	LDR R3,[R1,#8]
	ADCS R5,R2,R3
	
	LDR R2,[R0,#4]
	LDR R3,[R1,#4]
	ADCS R6,R2,R3
	
	LDR R2,[R0]
	LDR R3,[R1]
	ADCS R7,R2,R3
	ADCS R8,#0
	
	LDR R9,=RESULT
	STR R4,[R9],#4
	STR R5,[R9],#4
	STR R6,[R9],#4
	STR R7,[R9],#4
	STR R8,[R9],#4
	
STOP
	B STOP
VALUE1 DCD 0X12345678, 0XABCDEF12, 0X98765432, 0XABCD1234
