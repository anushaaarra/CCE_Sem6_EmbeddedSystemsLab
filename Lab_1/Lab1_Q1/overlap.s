	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors 
	DCD 0x10001000 
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	MOV R3, #10
	LDR R0,=DST+(N*4-4)
	LDR R2,=DST+(N+2*4-4)-OLP*4
UP	LDR R1,[R0],#4
	STR R1,[R2],#4
	SUBS R3,#1
	BNE UP
STOP
	B STOP
OLP EQU 5
N EQU 10
DST DCD 0X12345678, 0XABCDEF12, 0X98765432, 0X356334, 0X451239, 0X897234, 0X123874, 0X456746, 0X345632, 0X123459	
	END
	